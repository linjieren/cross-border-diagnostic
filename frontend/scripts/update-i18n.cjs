function deepMerge(target, source) {
  for (const key of Object.keys(source)) {
    if (source[key] && typeof source[key] === 'object' && !Array.isArray(source[key])) {
      if (!target[key] || typeof target[key] !== 'object') target[key] = {};
      deepMerge(target[key], source[key]);
    } else {
      target[key] = source[key];
    }
  }
  return target;
}

for (const lang of ['zh-CN', 'en', 'ja', 'ko']) {
  const filePath = path.join(localesDir, lang === 'zh-CN' ? 'zh-CN.json' : `${lang}.json`);
  const data = JSON.parse(fs.readFileSync(filePath, 'utf-8'));

  if (translations[lang]) {
    deepMerge(data, translations[lang]);
  }
  if (reportPageTranslations[lang]) {
    if (!data.report) data.report = {};
    deepMerge(data.report, reportPageTranslations[lang]);
  }

  fs.writeFileSync(filePath, JSON.stringify(data, null, 2) + '\n');
  console.log(`Updated ${filePath}`);
}

